<?php 
// casing
$items = 'Item';
$Items = 'Items';

// CamelCase
$myVariable - 'some string';
$myFirstName - 'some string';

// Underscore
// CANNOT DO THIS --> $my-variable = 'some string';
$my_variable = 'some string';

// Numbers 
// CANNOT DO THIS --> $3variable = 'some string';
$variable3 = 'some string';
// cannot start variable with number 

// Best Practice 
Avoid: 
 - hyphenated variable names ('$this-variable')
  - multiple undersccore 